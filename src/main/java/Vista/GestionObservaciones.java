    private void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tbObservaciones.getModel();
        modelo.setRowCount(0);
        try {
            List<Observacion> lista = controlObservacion.findObservacionEntities();
            for (Observacion o : lista) {
                Object[] fila = {
                    o.getIdObservacion(),
                    o.getIdRenta().getIdRenta(),
                    o.getDescripcion()
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarRentas() {
        cbRenta.removeAllItems();
        try {
            List<Renta> lista = controlRenta.findRentaEntities();
            for (Renta r : lista) {
                cbRenta.addItem(r.getIdRenta() + " - " + r.getIdVehiculo().getMarca() + " " + r.getIdVehiculo().getModelo());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            String rentaStr = (String) cbRenta.getSelectedItem();
            String descripcion = txtDescripcion.getText().trim();

            if (rentaStr == null || descripcion.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Seleccione una renta e ingrese una descripción.");
                return;
            }

            int idRenta = Integer.parseInt(rentaStr.split(" - ")[0]);
            Renta renta = controlRenta.findRenta(idRenta);

            Observacion observacion = new Observacion();
            observacion.setIdRenta(renta);
            observacion.setDescripcion(descripcion);

            controlObservacion.create(observacion);
            JOptionPane.showMessageDialog(this, "Observación agregada exitosamente.");
            txtDescripcion.setText("");
            cargarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar observación: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed


    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        try {
            String clienteStr = (String) cbCliente.getSelectedItem();
            String vehiculoStr = (String) cbVehiculo.getSelectedItem();
            String fechaInicioStr = txtFechaInicio.getText();
            String fechaFinStr = txtFechaFin.getText();

            if (clienteStr == null || vehiculoStr == null || fechaInicioStr.isEmpty() || fechaFinStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Complete todos los campos.");
                return;
            }

            int idCliente = Integer.parseInt(clienteStr.split(" - ")[0]);
            int idVehiculo = Integer.parseInt(vehiculoStr.split(" - ")[0]);
            java.sql.Date fechaInicio = java.sql.Date.valueOf(fechaInicioStr);
            java.sql.Date fechaFin = java.sql.Date.valueOf(fechaFinStr);

            Cliente cliente = controlCliente.findCliente(idCliente);
            Vehiculo vehiculo = controlVehiculo.findVehiculo(idVehiculo);

            Reservacion reservacion = new Reservacion();
            reservacion.setIdCliente(cliente);
            reservacion.setIdVehiculo(vehiculo);
            reservacion.setFechaInicio(fechaInicio);
            reservacion.setFechaFin(fechaFin);
            reservacion.setActiva(true);

            controlReservacion.create(reservacion);
            JOptionPane.showMessageDialog(this, "Reservación creada exitosamente.");
            cargarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al crear reservación: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCrearActionPerformed


    private void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tbReservaciones.getModel();
        modelo.setRowCount(0);
        try {
            List<Reservacion> lista = controlReservacion.findReservacionEntities();
            for (Reservacion r : lista) {
                Object[] fila = {
                    r.getIdReservacion(),
                    r.getIdCliente() != null ? r.getIdCliente().getNombre() : "N/A",
                    r.getIdVehiculo() != null ? r.getIdVehiculo().getMarca() + " " + r.getIdVehiculo().getModelo() : "N/A",
                    r.getFechaInicio(),
                    r.getFechaFin(),
                    r.getActiva() ? "Activa" : "Inactiva"
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarClientes() {
        cbCliente.removeAllItems();
        try {
            List<Cliente> lista = controlCliente.findClienteEntities();
            for (Cliente c : lista) {
                cbCliente.addItem(c.getIdCliente() + " - " + c.getNombre());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarVehiculos() {
        cbVehiculo.removeAllItems();
        try {
            List<Vehiculo> lista = controlVehiculo.findVehiculoEntities();
            for (Vehiculo v : lista) {
                cbVehiculo.addItem(v.getIdVehiculo() + " - " + v.getMarca() + " " + v.getModelo());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


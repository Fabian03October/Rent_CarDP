    private void cargarTabla() {
        if (controlRenta == null) return;
        DefaultTableModel modelo = (DefaultTableModel) tbRentas.getModel();
        modelo.setRowCount(0);
        try {
            List<Renta> lista = controlRenta.findRentaEntities();
            for (Renta r : lista) {
                Object[] fila = {
                    r.getIdRenta(),
                    r.getIdCliente().getNombre(),
                    r.getIdVehiculo().getMarca() + " " + r.getIdVehiculo().getModelo(),
                    r.getFechaInicio(),
                    r.getFechaFin(),
                    r.getTarifaDiaria(),
                    r.getEstado()
                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar rentas: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void cargarClientes() {
        if (controlCliente == null) return;
        cbCliente.removeAllItems();
        try {
            List<Cliente> lista = controlCliente.findClienteEntities();
            for (Cliente c : lista) {
                cbCliente.addItem(c.getIdCliente() + " - " + c.getNombre());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar clientes: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void cargarVehiculos() {
        if (controlVehiculo == null) return;
        cbVehiculo.removeAllItems();
        try {
            List<Vehiculo> lista = controlVehiculo.findVehiculoEntities();
            for (Vehiculo v : lista) {
                cbVehiculo.addItem(v.getIdVehiculo() + " - " + v.getMarca() + " " + v.getModelo());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar vehículos: " + e.getMessage());
            e.printStackTrace();
        }
    }

